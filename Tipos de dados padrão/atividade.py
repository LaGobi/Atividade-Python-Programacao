### Exemplo de Código em Python:
# Tipos Numéricos
inteiro = 10
flutuante = 5.5
# Operadores Numéricos
soma= inteiro + flutuante
produto = inteiro * flutuante
# Tipo Booleano
verdadeiro = True
falso = False
# Exibindo os resultados
print("Soma", soma)                     # Exibe: Soma: 15.5
print("Produto:", produto)              # Exibe: Produto: 55.0
print("Verdadeiro:", verdadeiro)        # Exibe: Verdadeiro: True
print("Falso:", falso)                  # Exibe: Falso: False