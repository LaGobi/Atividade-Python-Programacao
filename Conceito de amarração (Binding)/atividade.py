### Exemplo do Código em Python:
#Amarração
idade=30
print("Idade:", idade) #Exibe: Idade: 30

# Amarração de uma string a mesma variável
idade = "trinta"
print("idade em palavras:", idade)
#Exibe: Idade em palavras : trinta

#Amarração de um float a uma nova variável
altura = 1.75
print("Altura:",altura)  #Exibe: Altura: 1.75