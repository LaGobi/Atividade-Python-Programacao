### Exemplo de Código em Python:
# Definir variaveis
nome = "João"
idade = 25
altura = 1.75

# Formatação usando f-strings (Python 3.6+)
saida_formatada = f"Nome: {nome}, Idade: {idade}, Altura: {altura:.2f}m"

# Exibir a saída formatada
print(saida_formatada)

### Exemplo de Saída:
#Nome: Joao, Idade: 25, Altura: 1.75m
